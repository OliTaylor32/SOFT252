    private void btn_requestAccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_requestAccActionPerformed
        File textFile = new File("D://SOFT252//SOFT252//Coursework//Data.txt");
        try
        {
            try (FileWriter write = new FileWriter(textFile, true)) {
                write.write(System.getProperty( "line.separator" ));
                write.write("AccRequest"); 
                write.write(System.getProperty( "line.separator" ));
                write.write(txt_password.getText());
                write.write(System.getProperty( "line.separator" ));
                write.write(txt_fName.getText());
                write.write(System.getProperty( "line.separator" ));
                write.write(txt_lName.getText());
                write.write(System.getProperty( "line.separator" ));
                write.write(txt_address.getText());
                write.write(System.getProperty( "line.separator" ));
                write.write(txt_age.getText());
                write.write(System.getProperty( "line.separator" ));
                write.write(cbx_gender.getItemAt(cbx_gender.getSelectedIndex()).charAt(0));
            }
        } 
        catch (IOException ex) 
        {
            Logger.getLogger(CreateAcc.class.getName()).log(Level.SEVERE, null, ex);
        }

        
    }//GEN-LAST:event_btn_requestAccActionPerformed

